public class PengulanganFor {
	public static void main (String[] args){
		for(int angka = 1; angka <=5; angka++){ //Untuk berlanjut dari 1 sampai 5
			System.out.println("Jalan ke-" +angka);
		}
		System.out.println("");
		System.out.println("");
		
		for(int angka = 5; angka >=1; angka--){ //Untuk kembali dari 5 sampai 1
			System.out.println("Jalan ke-" +angka);
		}	
	}	
}	